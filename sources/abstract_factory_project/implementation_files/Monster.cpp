Monster& Monster::operator= ( const Monster &other )
{
    if (this != &other)
    {
        Enemy::operator=(other);
    }
    return *this;
} // assignment operator

Monster& Monster::operator= ( Monster && other )
{
    if (this != &other)
    {
        Enemy::operator=(std::move(other));

    }
    return *this;
} // assignment-move operator

