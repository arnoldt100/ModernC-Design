SillyMonster& SillyMonster::operator= ( const SillyMonster &other )
{
    if (this != &other)
    {
        Monster::operator=(other);
    }
    return *this;
} // assignment operator

SillyMonster& SillyMonster::operator= ( SillyMonster && other )
{
    if (this != &other)
    {
        Monster::operator=(std::move(other));
    }
    return *this;
} // assignment-move operator

